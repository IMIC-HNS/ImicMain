export interface Policy
{
    /**
     * @type {number} id for storing the id of each policy
     * @memberof Policy
     */
    id:number,

    /**
     * @type {string} description for storing the description of each policy
     * @memberof Policy
     */
    description:string,

    /**
     * @type {string} name for storing the name of each policy
     * @memberof Policy
     */
    name:string
}